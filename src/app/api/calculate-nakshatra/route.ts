function getCoordinatesFromPlace(place: string): { lat: number; lon: number } {
  const normalizedPlace = place.toLowerCase().trim();
  
  // Try to find exact match first
  if (cityCoordinates[normalizedPlace]) {
    return cityCoordinates[normalizedPlace];
  }
  
  // Try to find partial match
  for (const [city, coords] of Object.entries(cityCoordinates)) {
    if (normalizedPlace.includes(city) || city.includes(normalizedPlace)) {
      return coords;
    }
  }
  
  // Default to Delhi if not found
  return { lat: 28.6139, lon: 77.2090 };
}

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { birth_date, birth_time, birth_place } = body;

    if (!birth_date || !birth_time || !birth_place) {
      return NextResponse.json(
        { error: "Missing required fields: birth_date, birth_time, birth_place" },
        { status: 400 }
      );
    }

    // Get coordinates from place name
    const coords = getCoordinatesFromPlace(birth_place);

    // Call the FastAPI backend for real astronomical calculations
    const backendResponse = await fetch(`${BACKEND_URL}/api/charts/calculate`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        name: "User",
        birth_date: birth_date,
        birth_time: birth_time,
        birth_place: birth_place,
        latitude: coords.lat,
        longitude: coords.lon,
      }),
    });

    if (!backendResponse.ok) {
      const errorText = await backendResponse.text();
      console.error("Backend error:", errorText);
      return NextResponse.json(
        { error: "Failed to calculate from backend" },
        { status: 500 }
      );
    }

    const data = await backendResponse.json();
    const chartData = data.chart_data;

    // Extract nakshatra information from the chart data
    return NextResponse.json({
      nakshatra: chartData.nakshatra,
      pada: chartData.nakshatra_pada,
      moon_sign: chartData.moon_sign,
      moon_sign_sanskrit: chartData.moon_sign_sanskrit,
      nakshatra_lord: chartData.nakshatra_lord,
      nakshatra_deity: chartData.nakshatra_deity,
      calculation_method: chartData.calculation_method,
    });
  } catch (error) {
    console.error("Error calculating nakshatra:", error);
    return NextResponse.json(
      { error: "Internal server error" },
      { status: 500 }
    );
  }
}
