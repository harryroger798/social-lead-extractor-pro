export function getCoordinatesFromPlace(place: string): { lat: number; lon: number } {
  const normalizedPlace = place.toLowerCase().trim();
  
  // Try to find exact match first
  if (cityCoordinates[normalizedPlace]) {
    return cityCoordinates[normalizedPlace];
  }
  
  // Try to find partial match
  for (const [city, coords] of Object.entries(cityCoordinates)) {
    if (normalizedPlace.includes(city) || city.includes(normalizedPlace)) {
      return coords;
    }
  }
  
  // Default to Delhi if not found
  return { lat: 28.6139, lon: 77.2090 };
}
