def get_translations(lang: str = "en") -> dict[str, str]:
    """Get translations for a language. Falls back to English for missing keys."""
    en = TRANSLATIONS.get("en", {})
    target = TRANSLATIONS.get(lang, {})

    # Merge: target overrides English defaults
    merged = {**en, **target}
    return merged


def get_supported_languages() -> list[dict]:
    """Return list of supported languages."""
    return SUPPORTED_LANGUAGES


def translate(key: str, lang: str = "en") -> str:
    """Translate a single key. Falls back to English, then to the key itself."""
    translations = get_translations(lang)
    return translations.get(key, TRANSLATIONS.get("en", {}).get(key, key))
